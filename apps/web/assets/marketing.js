  async function readApiError(response) {
    try {
      const payload = await response.json();
      const detail = Array.isArray(payload?.detail)
        ? payload.detail
            .map((item) => item?.msg || item?.message || "")
            .filter(Boolean)
            .join(" ")
        : typeof payload?.detail === "string"
          ? payload.detail
          : "";
      if (/high-risk medical, certification, or absolute/i.test(detail)) {
        return copy[locale].claimError;
      }
    } catch {
      // The localized generic message below is safer than exposing a raw server response.
    }
    return copy[locale].genericError;
  }

